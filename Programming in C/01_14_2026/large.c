//Find the largest of 2 numbers

#include <stdio.h>
int main()
{
	int num1, num2;
        printf("Enter value of num1: ");
        scanf("%d", &num1);
        printf("Enter value of num2: ");
        scanf("%d", &num2);
	if(num1 > num2)
	{
		printf("%d is greater than %d", num1, num2);
	}
	else
	{
		printf("%d is greater than %d", num2, num1);
	}

	return 0;
}
